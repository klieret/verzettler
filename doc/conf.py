from pathlib import Path
import shutil

this_dir = Path(__file__).parent.resolve()

readme_path = this_dir.parent / "README.md"
readme_target = this_dir / "readme.md"
readme_assets_path = this_dir.parent / "readme_assets"
readme_assets_target = this_dir / "readme_assets"

with readme_target.open("w") as outf:
    outf.write(
        "\n".join(
            [
                "Readme",
                "======",
            ]
        )
    )
    lines = []
    for line in readme_path.read_text().split("\n"):
        if line.startswith("# "):
            # Skip title, because we now use "Readme"
            continue
        lines.append(line)
    outf.write("\n".join(lines))
if readme_assets_target.exists():
    shutil.rmtree(readme_assets_target)
shutil.copytree(readme_assets_path, readme_assets_target)

